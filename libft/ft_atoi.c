#include "../push_swap.h"

int	ft_atoi(char *s, int *tab)
{
	int long	n;
	int			sign;

	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	sign = 1;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sign *= -1;
		s++;
	}
	n = 0;
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + (*s - '0');
		if ((n * sign > 2147483647) || (n * sign < -2147483648))
			return (free(tab), ft_throw_error(), 0);
		s++;
	}
	return (n * sign);
}
